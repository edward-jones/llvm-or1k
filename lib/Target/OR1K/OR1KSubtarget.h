#ifndef OR1KSUBTARGET_H
#define OR1KSUBTARGET_H

#include "llvm/Target/TargetMachine.h"
#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "OR1KGenSubtargetInfo.inc"

namespace llvm {

class OR1KSubtarget : public OR1KGenSubtargetInfo {
public:
  enum ABIKind { DefaultABI, NewABI };

  enum class DelayType { Delay, NoDelay, CompatDelay };

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  OR1KSubtarget(const std::string &TT, const std::string &CPU,
                const std::string &FS, bool LittleEndian);

  /// \brief Parses features string setting specified subtarget options.
  /// Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  bool hasMul() const { return HasMul; }
  bool hasMul64() const { return HasMul64; }
  bool hasDiv() const { return HasDiv; }
  bool hasRor() const { return HasRor; }
  bool hasCmov() const { return HasCmov; }
  bool hasMAC() const { return HasMAC; }
  bool hasExt() const { return HasExt; }
  bool hasSFII() const { return HasSFII; }
  bool hasFBit() const { return HasFBit; }
  DelayType delaySlotType() const { return DelaySlotType; }
  bool isLittleEndian() const { return IsLittleEndian; }

  bool isDefaultABI() const { return OR1KABI == DefaultABI; }
  bool isNewABI() const { return OR1KABI == NewABI; }

  const InstrItineraryData &getInstrItineraryData() const { return InstrItins; }

  bool enablePostRAScheduler(CodeGenOpt::Level OptLevel, AntiDepBreakMode &Mode,
                             RegClassVector &CriticalPathRCs) const override;

  bool enableMachineScheduler() const override { return true; }

private:
  virtual void anchor();

private:
  ABIKind OR1KABI;
  InstrItineraryData InstrItins;
  bool HasMul;
  bool HasMul64;
  bool HasDiv;
  bool HasRor;
  bool HasCmov;
  bool HasMAC;
  bool HasExt;
  bool HasSFII;
  bool HasFBit;
  DelayType DelaySlotType;
  bool IsLittleEndian;
};
} // end llvm namespace

#endif
